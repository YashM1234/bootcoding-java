package com.bootcoding.java.flowcontrol;
//WAP to find the given number is even or odd.
public class Assignment1EvenOddFinder {
    public static void main(String[] args) {
        int num1 = 10;
        if(num1 % 2 == 0){
            System.out.println("The given number is " + num1 + " and it is even.");
        }
        else{
            System.out.println("The given number is " + num1 + " and it is odd.");
        }
    }
}
