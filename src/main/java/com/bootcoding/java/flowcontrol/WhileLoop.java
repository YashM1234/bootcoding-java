package com.bootcoding.java.flowcontrol;
//While Loop in Java
public class WhileLoop {
    public static void main(String[] args) {
        /*
        Syntax: while(condition is true){
                    //body of loop
                 }
         */
        //WAP to print numbers from 1 to given number
        int i = 1;
        int ui = 5;
        while(i <= ui){
            System.out.println(i);
            i++;
        }
    }
}
